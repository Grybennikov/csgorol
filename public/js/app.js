'use strict';
var app= angular.module('gameApp',['ngRoute','ngAnimate','ksSwiper','ngDialog', 'ngNotify', 'pascalprecht.translate', 'ngCookies', 'timer']);

app.config(function ($routeProvider) {
	$routeProvider
		.when('/',{
			templateUrl:'./templates/main.html',
			controller:'homeCtrl'
		})
		.when('/top',{
			templateUrl:'./templates/top.html',
			controller:'topCtrl'
		})
		.when('/history',{
			templateUrl:'./templates/history.html',
			controller:'historyCtrl'
		})
		.when('/try',{
			templateUrl:'./templates/try.html',
			controller:'tryCtrl'
		})
		.when('/about',{
			templateUrl:'./templates/about.html',
			controller:'aboutCtrl'
		})
		.when('/game/:gameId', {
			templateUrl:'./templates/game.html',
			controller : 'gameCtrl'
		})
		.when('/my/profile', {
			templateUrl : './templates/my-profile.html',
			controller : 'myProfileCtrl'
		})
		.when('/my/history', {
			templateUrl : './templates/my-history.html',
			controller : 'myHistoryCtrl'
		})
		.when('/my/inventory', {
			templateUrl : './templates/my-inventory.html',
			controller : 'myInventoryCtrl'
		})
		.when('/support', {
			templateUrl : './templates/support.html',
			controller : 'supportCtrl'
		})
		.when('/faq', {
			templateUrl : './templates/faq.html',
			controller : 'faqCtrl'
		})
		.otherwise({
			redirectTo : '/'
		})
});

